# Comentarios: Se utilizan con el símbolo "#" y no afectan la ejecución del código
# Ejemplo de comentario

# Imprimir texto en pantalla: función print()
print("¡Hola, mundo!")  # Esto mostrará: ¡Hola, mundo!

# Variables
nombre = "Juan"  # Variable de tipo string o cadena
edad = 25          # Variable de tipo entero
altura = 1.75      # Variable de tipo flotante
es_estudiante = True  # Variable de tipo booleano

print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("¿Es estudiante?", es_estudiante)

# Entrada de datos por parte del usuario
# input() siempre devuelve un string
usuario = input("¿Cuál es tu nombre? ")
print("Hola,", usuario, "bienvenido a Python!")
